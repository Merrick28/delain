                <br><br>
Les souterrains de  Delain est un  jeu gratuit mis  à votre disposition  par ses
créateurs. La  présente charte  a pour  but d'établir  quelques règles  de bonne
conduite pour permettre à tous  les participants de s'amuser pleinement.  Il est
indispensable que tout  joueur respsecte ces  quelques règles pour  l'intérêt de
tous. <strong>Tout  manquement ou  tout abus  fera l'objet de  sanctions pouvant aller
jusqu'à la suppression définitive du compte du joueur.</strong>
<br><br>
<hr><strong><u>(1) Inscription :</strong></u>
<br><br>La participation au jeu est entièrement gratuite et l'inscription est ouverte à tous les internautes disposant d'une adresse Email personnelle et active et d'un accès à Internet régulier.
Il n'y a pas à proprement parler d'age minimum imposé pour pouvoir jouer à Delain, par contre, nous déconseillons aux enfants de moins de 12 ans de jouer.
En effet, Delain n'est pas un jeu à but pédagogique et chaque joueur doit être capable de gérer seul son personnage sans être assisté d'un tiers.
Si vous êtes plusieurs joueurs à partager la même connexion, vous devez impérativement le déclarer aux contrôleurs en envoyant un mail à <a href="controleurs@jdr-delain.net">controleurs@jdr-delain.net</a>.
<br><br>
<hr><strong><u>(2) Email :</strong></u>
<br><br>Par votre inscription au jeu, vous autorisez l'envoi d'Emails de la part du moteur et des gestionnaires du jeu à l'adresse Email que vous aurez fournie lors de votre inscription. Par défaut, le moteur du jeu nenvoie pas de message, libre au joueur de configurer son compte pour lémission de la majeure partie de ces Emails.
Il est indispensable que le joueur dispose d'une adresse Email qui lui est propre et auquel il accède régulièrement, ce moyen pouvant être utilisé par les gestionnaires du jeu pour le contacter.
<br><br>
<hr><strong><u>(3) Données Personnelles :</strong></u>
<br><br>Vous pouvez à tout moment demander à consulter, modifier et/ou supprimer les informations qui vous concernent. Une copie complète des informations vous concernant en notre possession peut vous être communiquée sur simple demande.
Néanmoins, certaines informations enregistrées dans notre base de données devant impérativement rester confidentielles pour le bon fonctionnement du jeu, le joueur demandant la suppression d'une partie ou de toutes les informations de ses Personnages ne pourra continuer à jouer avec ceux-ci.
Toutes les données du jeu sont consultables par l'Equipe du jeu, sans restriction.
<br><br>
<hr><strong><u>(4) Saisies :</strong></u>
<br><br>Tout comportement qui sera jugé comme une atteinte à la morale entraînera l'élimination immédiate et sans préavis du compte du joueur; Ceci inclue, de manière non exhaustive, les Forums, le Chat, les Messages envoyés aux autres joueurs, les Noms et Descriptifs de Personnages, de Guildes créés ou modifiés par le joueur. Les Emails envoyés à d'autres joueurs et seront également soumis à cette règle.
Il est également interdit d'y faire de la publicité pour un quelconque produit ou une quelconque marque.
<br><br>
<hr><strong><u>(5) Vente de comptes :</strong></u>
<br><br>La vente pour de l'argent ou des biens matériels d'un compte et/ou d'éléments du jeu est formellement interdite.
<br><br>
<hr><strong><u>(6) Délais d'inactivité :</strong></u>
<br><br>Les Personnages vivants seront supprimés après 30 jours d'inactivité, hors hibernation. Les personnages en hibernation sont supprimés après un délai de 90 jours.
<br><br>
<hr><strong><u>(7) Multi-compte :</strong></u>
<br><br>Chaque joueur ne peut posséder qu'un seul et unique compte. Un joueur ne peut donc inscrire et jouer que les trois personnages de ce compte, ainsi que leurs éventuels familiers. Si un cas de multi-compte (le fait de jouer plusieurs comptes de Personnages) est décelé et confirmé, tous les comptes incriminés seront purement et simplement supprimés.
<br>En clair : Un compte pour un joueur, pas X comptes pour X joueurs. (Il existe une seule exception : Un seul compte pour deux joueurs)
<br><br>
<hr><strong><u>(8 ) Sitting :</strong></u>
<br><br>Pour plus de convivialité, il est autorisé occasionnellement de jouer le ou les personnages d'un ami s'il ne peut jouer lui même. L'occasionnel se transformant en habitude sera considéré comme du multi-compte : chaque joueur doit toujours rester seul maître de ses personnages.
<br><strong>La communication du mot de passe d'un compte par son propriétaire est sous la seule responsabilité du joueur</strong>. Se connecter sur un autre compte que le sien est considèré comme un sitting, qu'il y'ai une action faite ou pas.

<ul><li>1º/ Les sittings sont autorisés de manière occasionnelle et ne peuvent en aucun cas excéder 5 jours par période de 15 jours.

<li>2º/ Au delà de 5 jours consécutifs, les personnages doivent hiberner ou être abandonnés jusqu'à ce que le joueur propriétaire du compte le reprenne en charge. Les simples « visites » de sitteurs ne sont pas autorisées dans cette période.

<li>3°/ Les relais de sitteurs de 5 en 5 jours par des joueurs différents sont évidemment strictement interdits.

<li>4º/ Les joueurs sortant du cadre de ces règles doivent impérativement contacter les contrôleurs qui analyseront alors chaque cas particulier.

<li>5°/ Le "sitting Tactique" ( i.e. pour synchroniser idéalement les groupes de combat ) est à proscrire car il s'apparente trop à du multi-compte.

<li>6º/ Tout sitting sortant du cadre des règles précédentes sera considéré comme abusif et passible de lourdes sanctions, allant jusqu'à la suppression de compte.</ul>
<br><br>
<hr><strong><u>(9) Suicide daventuriers :</strong></u>
<br><br>Les Personnages pouvant se tuer entre eux, il est interdit à un joueur de laisser tuer son personnage volontairement par un autre joueur ou d'organiser la mort de son Personnage afin de faire profiter de la situation et/ou de l'expérience acquise à l'occasion de cette mort.
Les contrevenants à cette clause sexposent à des sanctions. Toutefois, en cas dabandon définitif de personnage, le matériel peut être abandonné au profit dautres personnages.
<br><br>
<hr><strong><u>(10) Messagerie interne :</strong></u>
<br><br>Dans le cadre du jeu, une messagerie interne est mise à disposition de tous les Personnages. Cette messagerie est une composante du jeu et des interactions entre Personnages, elle n'a pas pour vocation à être utilisée à des fins privées et personnelles en dehors du contexte du jeu.
<br><br>
<hr><strong><u>(11) Contrôleurs :</strong></u>
<br><br>Les Contrôleurs sont les arbitres officiels du jeu. Toute situation litigieuse non mentionnée dans la charte sera laissée à leur jugement . Les Contrôleurs disposent du droit de sanctionner (voire de supprimer) n'importe quel personnage s'ils le jugent nécessaire.
Les joueurs disposent néanmoins d'un droit de recours auprès du gestionnaire du projet Delain mais ne doivent pas en abuser.
Dans le cours dune enquête, il est indispensable que les joueurs se montrent coopératifs et ne se laissent pas aller à la vulgarité et lironie grinçante, sous peine dalourdissement de la sanction.
Lors d'une enquête, soyez toujours correct avec léquipe de Contrôle : raleries, énervement et remontrances ne servent à rien si ce n'est à alourdir les sanctions.
En cas d'insulte à l'encontre de l'enquêteur, le(s) compte(s) incriminé(s) sera (seront) purement et simplement supprimé(s).
La complicité de Multi-compte et le mensonge lors dune enquête sont également passibles de lourdes sanctions.
Léquipe de contrôle prendra toujours contact avec les joueurs suspectés, et aucune décision ou sanction ne sera prise sans laisser un temps de justification aux suspectés (sauf en cas dabsence volontaire de réponse).
<br><br>
<hr><strong><u>(12) Délation :</strong></u>
<br><br>La délation publique d'une infraction est proscrite et sera réprimée. Pour pallier à cela, nous encourageons les joueurs à signaler aux contrôleurs une situation qui leur semble en infraction avec la présente charte par mail à : <a href="controleurs@jdr-delain.net">controleurs@jdr-delain.net</a>.
Les gestionnaires de guilde doivent être particulièrement attentifs sur ce point car leur collaboration dans une enquête pourra être requise.
Un administrateur de guilde fermant volontairement les yeux sur une infraction à la charte risque les mêmes sanctions que les joueurs en infraction.
<br><br>
<hr><strong><u>(13) Automatisation :</strong></u>
<br><br>Un Personnage doit être contrôlé par un joueur : il est formellement interdit d'automatiser les actions par quelque moyen que ce soit (script, programme, site web, etc.). Ceci inclut également l'interdiction d'activer automatiquement le calcul d'un nouveau tour de jeu (Activation de DLT).
<br><br>
<hr><strong><u>(14) Bugs :</strong></u>
<br><br>Delain est un développement amateur fait par des personnes 100% bénévoles. Aussi, le jeu n'est pas à l'abri de bugs et incohérences. Chaque joueur est tenu de rapporter (par mail ou sur le forum ad hoc) au plus vite tout problème décelé, et toute utilisation abusive ou volontaire d'un bug ou d'une faille se verra sanctionnée et pourra se solder par la suppression du compte du joueur concerné.
De même, le jeu et ses règles sont susceptibles dévoluer avec le temps.
Les compensations en cas de bug ou de modification du moteur du jeu ne sont en rien un dû, et pourront être refusées sans explications, Delain étant un jeu en constante évolution.
<br><br>
<hr><strong><u>(15) Charte d'admission :</strong></u>
<br><br>La présente charte doit être lue et acceptée dans son intégralité pour pouvoir entrer dans les souterrains de Delain.
Elle est susceptible d'être modifiée par les gestionnaires du jeu.
<br>Le cas échéant, il sera demandé aux joueurs d'accepter explicitement les modifications apportées pour pouvoir continuer à jouer.
<br>Tout comportement semblant enfreindre cette charte peut être rapporté par mail aux controleurs : <a href="controleurs@jdr-delain.net">controleurs@jdr-delain.net</a>
